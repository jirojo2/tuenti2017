# parse number
def parse_number(data):
    number = []
    state = 0
    for l in data:
        #u = unicode(l, errors='replace')
        u = unichr(ord(l))
        if u in digits and state <= 1:
            if state == 0:
                state = 1
            if state == 1:
                number.append(digits[u])
        elif u in spaces:
            if state == 1:
                state = 2
        else:
            return None
    value = 0
    for i, d in enumerate(number):
        value += d * 10**(len(number)-1-i)
    return value

